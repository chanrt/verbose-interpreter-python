import math

def add(a, b):
    return a + b

def subtract(a, b):
    return a - b

def multiply(a, b):
    return a * b

def divide(a, b):
    return a / b

def pow(a, b):
    return math.pow(a, b)

def sqrt(a):
    return math.sqrt(a)

def sin(a):
    return math.sin(a)

def cos(a):
    return math.cos(a)

def tan(a):
    return math.tan(a)

def cosec(a):
    return 1 / math.sin(a)

def sec(a):
    return 1 / math.cos(a)

def cot(a):
    return 1 / tan(a)